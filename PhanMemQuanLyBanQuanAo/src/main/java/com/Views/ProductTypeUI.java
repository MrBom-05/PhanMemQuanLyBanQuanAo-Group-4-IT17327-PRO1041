    private void btAdd3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAdd3ActionPerformed
        ProductType productType = getFormData();
        if (productType == null) {
            return;
        }
        productTypeService.insert(productType);
        LoadData(productTypeService.getList());
        JOptionPane.showMessageDialog(this, "Thêm thành công");
    }//GEN-LAST:event_btAdd3ActionPerformed

    private void tblProductTypeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProductTypeMouseClicked
        int row = this.tblProductType.getSelectedRow();
        txtId.setText(tblProductType.getValueAt(row, 0).toString());
        txtMaSanPham.setText(tblProductType.getValueAt(row, 1).toString());
        txtTenSanPham.setText(tblProductType.getValueAt(row, 2).toString());
    }//GEN-LAST:event_tblProductTypeMouseClicked

    private void btFixActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btFixActionPerformed
        int row = this.tblProductType.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng để sửa");
            return;
        }
        ProductType productType = getFormData();
        if (productType == null) {
            return;
        }
        productTypeService.update(productType, txtTenSanPham.getText());
        LoadData(productTypeService.getList());
        JOptionPane.showMessageDialog(this, "Sửa thành công");
    }//GEN-LAST:event_btFixActionPerformed

    private void btDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btDeleteActionPerformed
        int row = this.tblProductType.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn dòng để xóa");
            return;
        }
        int comfirm = JOptionPane.showConfirmDialog(this, " Bạn muốn xóa không ?");
        if (comfirm == JOptionPane.NO_OPTION) {
            return;
        } else if (comfirm == JOptionPane.CANCEL_OPTION) {
            return;
        } else {
            productTypeService.delete(txtId.getText());
            LoadData(productTypeService.getList());
            JOptionPane.showMessageDialog(this, "Xóa thành công");
        }
    }//GEN-LAST:event_btDeleteActionPerformed

