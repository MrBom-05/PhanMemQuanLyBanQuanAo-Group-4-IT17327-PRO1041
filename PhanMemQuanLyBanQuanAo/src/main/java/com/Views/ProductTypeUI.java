    public void LoadData(List<ProductType> listColor) {
        DefaultTableModel dtm = (DefaultTableModel) this.tblProductType.getModel();
        dtm.setRowCount(0);
        for (ProductType productType : listColor) {
            Object[] row = new Object[]{
                productType.getId(),
                productType.getCode(),
                productType.getName(),};
            dtm.addRow(row);
        }
    }

    public ProductType getFormData() {
        String ma = txtMaSanPham.getText();
        String ten = txtTenSanPham.getText();
        ProductType productType = new ProductType(ma, ten);
        return productType;
    }

